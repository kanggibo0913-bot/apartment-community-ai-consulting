// 공통 항목 빌더 (비품 외 카테고리)
const item = (
  id: string,
  category: OpeningChecklistItem['category'],
  title: string,
  priority: ChecklistPriority,
): OpeningChecklistItem => ({
  id,
  category,
  title,
  description: '',
  status: '미확인',
  assignee: '',
  dueDate: '',
  completedAt: '',
  priority,
  memo: '',
})

// 비품 항목 빌더 (수량/단위/구매상태 기본값 포함)
const supply = (id: string, title: string, unit: string, priority: ChecklistPriority): OpeningChecklistItem => ({
  ...item(id, '비품', title, priority),
  quantityNeeded: 0,
  quantityReady: 0,
  unit,
  supplier: '',
  purchaseStatus: '미구매',
})

